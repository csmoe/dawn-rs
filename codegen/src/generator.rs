use heck::{ToPascalCase, ToSnakeCase};

use crate::{
    Annotation, BitmaskDef, DawnApi, FunctionDef, MethodDef, ObjectDef, RecordMember, ReturnType,
    StructureDef,
};

pub trait Codegen {
    fn codegen(&self) -> String;
}

impl DawnApi {
    pub fn codegen(self) -> String {
        let objects = self
            .objects()
            .iter()
            .map(|(name, def)| Object { name, def }.codegen())
            .collect::<Vec<_>>()
            .join("\n");
        let functions = self
            .functions()
            .iter()
            .map(|(name, def)| Function { name, def }.codegen())
            .collect::<Vec<_>>()
            .join("\n");
        let structs = self
            .structures()
            .iter()
            .map(|(name, def)| Structure { name, def }.codegen())
            .collect::<Vec<_>>()
            .join("\n");
        let DawnApi { comment, doc, .. } = self;
        let comment = comment
            .unwrap_or_default()
            .iter()
            .map(|c| format!("/// {c}"))
            .collect::<Vec<_>>()
            .join("\n");
        let doc = doc
            .as_ref()
            .map(|d| format!("/// {}", d))
            .unwrap_or_default();
        let version = env!("CARGO_PKG_VERSION");

        let content = format!(
            r#"
{comment}
{doc}

/// Generated by dawn-codegen {version}

{structs}

{objects}

{functions}
"#
        );
        //content
        let source = syn::parse_file(&content).unwrap();
        prettyplease::unparse(&source)
    }
}

struct Function<'a> {
    name: &'a str,
    def: &'a FunctionDef,
}

impl Codegen for Function<'_> {
    fn codegen(&self) -> String {
        let FunctionDef {
            returns,
            args,
            comment,
            ..
        } = self.def;
        let name = self.name.to_snake_case();
        let ret = returns.codegen();
        let comment = comment
            .as_ref()
            .map(|c| format!("/// {}", c))
            .unwrap_or_default();
        let args = args
            .iter()
            .map(|arg| {
                let (_, tys, _) = arg.codegen();
                tys
            })
            .collect::<Vec<_>>()
            .join(",");

        format!(
            r#"
{comment}
pub fn {name}({args}) -> {ret} {{
}}
"#
        )
    }
}

impl Codegen for Option<ReturnType> {
    fn codegen(&self) -> String {
        match self {
            Some(ReturnType::Complex {
                return_type,
                optional,
            }) => {
                let ty = return_type.to_pascal_case();
                if *optional {
                    format!("Option<{ty}>")
                } else {
                    format!("{ty}")
                }
            }
            Some(ReturnType::Simple(return_type)) => {
                let ty = return_type.to_pascal_case();
                format!("{ty}")
            }
            None => "()".to_string(),
        }
    }
}

impl RecordMember {
    fn codegen(&self) -> (String, String, String) {
        let RecordMember {
            name,
            member_type,
            annotation,
            length,
            optional,
            default,
            wire_is_data_only,
            skip_serialize,
            no_default,
            array_element_optional,
        } = &self;
        let arg_name = if name.to_lowercase() == "type" {
            "r#type".to_string()
        } else {
            name.to_snake_case()
        };

        let mut arg_ty = match member_type.as_str() {
            "long" => "i64".to_string(),
            "uint16_t" => "u16".to_string(),
            "int16_t" => "i16".to_string(),
            "uint32_t" | "uint" => "u32".to_string(),
            "int32_t" | "int" => "i32".to_string(),
            "uint64_t" => "u64".to_string(),
            "int64_t" => "i64".to_string(),
            "bool" => "bool".to_string(),
            "float" => "f32".to_string(),
            "double" => "f64".to_string(),
            "size_t" => "usize".to_string(),
            _ => member_type.to_pascal_case(),
        };

        if *optional {
            arg_ty = format!("Option<{arg_ty}>");
        }
        let mut default_code = "".to_string();
        let mut arg_stmt = "".to_string();
        if !annotation.is_value() {
            if no_default.unwrap_or_default() {
                arg_ty = if annotation.is_const_ptr() {
                    format!("&{arg_ty}")
                } else {
                    format!("&mut {arg_ty}")
                }
                .to_string();
            } else {
                default_code = {
                    let default = match default {
                        Some(v) => v.to_string(),
                        None => {
                            if annotation.is_const_ptr() {
                                format!("std::ptr::null()")
                            } else {
                                format!("std::ptr::null_mut()")
                            }
                        }
                    };
                    format!(".unwrap_or_else(|| {default})")
                }
            };
            arg_stmt = format!("let {arg_name} = {arg_name}{default_code};");
        }
        //let annotation = annotation.codegen();

        let arg = format!("{arg_name}: {arg_ty},");
        (arg_name + ",", arg, arg_stmt)
    }
}

impl Codegen for Annotation {
    fn codegen(&self) -> String {
        match self {
            Annotation::MutPtr => "*mut ",
            Annotation::ConstPtr => "*const ",
            Annotation::ConstConstPtr => "*const *const ",
            Annotation::Value => "",
        }
        .to_string()
    }
}

struct Method<'a> {
    self_: &'a Object<'a>,
    def: &'a MethodDef,
}

impl Codegen for Method<'_> {
    fn codegen(&self) -> String {
        let MethodDef {
            name,
            tags,
            returns,
            args,
            no_autolock,
            extensible,
        } = &self.def;
        let name = name.to_snake_case();
        let ret = returns.codegen();

        let safety = if no_autolock.unwrap_or_default() {
            r#"/// <div class="warning">
/// This method is not thread-safe
/// </div>"#
        } else {
            ""
        };

        let object_name = self.self_.name.to_pascal_case();
        let c_name = name.to_pascal_case();
        let args_names = args
            .iter()
            .map(|arg| {
                let (name, ..) = arg.codegen();
                name
            })
            .collect::<Vec<_>>()
            .join("");
        let args_types = args
            .iter()
            .map(|arg| {
                let (_, tys, _) = arg.codegen();
                tys
            })
            .collect::<Vec<String>>()
            .join("");
        let args_stms = args
            .iter()
            .map(|arg| {
                let (_, _, stmt) = arg.codegen();
                stmt
            })
            .collect::<Vec<String>>()
            .join("");
        format!(
            r#"{safety}
pub fn {name}(&self, {args_types}) -> {ret} {{
{args_stms}
    unsafe {{
        wgpu{object_name}{c_name}(self, {args_names})
    }}
}}"#
        )
    }
}

struct Object<'a> {
    name: &'a str,
    def: &'a ObjectDef,
}

impl Codegen for Object<'_> {
    fn codegen(&self) -> String {
        let ObjectDef {
            tags,
            methods,
            no_autolock,
        } = &self.def;
        let name = self.name.to_pascal_case();
        let methods = methods
            .iter()
            .map(|method| {
                Method {
                    self_: self,
                    def: method,
                }
                .codegen()
            })
            .collect::<Vec<String>>()
            .join("\n");
        let thread_safety = if no_autolock.unwrap_or_default() {
            format!("unsafe impl !Send for {name} {{}}\nunsafe impl !Sync for {name} {{}}")
        } else {
            format!("unsafe impl Send for {name} {{}}\nunsafe impl Sync for {name} {{}}")
        };
        format!(
            r#"
pub struct {name};

impl Clone for {name} {{
    fn clone(&self) -> Self {{
        unsafe {{
            wgpu{name}AddRef(self)
        }}
    }}
}}

impl Drop for {name} {{
    fn drop(&mut self) {{
        unsafe {{
            wgpu{name}Release(self)
        }}
    }}
}}

{thread_safety}

impl {name} {{
    {methods}
}}"#
        )
    }
}

struct Structure<'a> {
    name: &'a str,
    def: &'a StructureDef,
}

impl Codegen for Structure<'_> {
    fn codegen(&self) -> String {
        let StructureDef {
            tags,
            members,
            comment,
            ..
        } = &self.def;
        let name = self.name.to_pascal_case();
        let fields = members
            .iter()
            .map(|field| {
                let (_, tys, _) = field.codegen();
                tys
            })
            .collect::<Vec<String>>()
            .join("");
        let comment = comment
            .as_ref()
            .map(|comment| format!("/// {}", comment))
            .unwrap_or_default();
        format!(
            r#"
{comment}
pub struct {name} {{
    {fields}
}}"#
        )
    }
}

